# 算数运算符
a = 10
b = 20
print(a + b)  # +
print(a - b)  # -
print(a * b)  # *
print(b / a)  # /
print(b // a)  # 取整
print(b % a)  # 取余
print(a ** b)  # 指数

# 赋值运算符
num_var = 10
print(num_var)
# 多个变量赋值
num1, num2, f1, str1 = 100, 200, 3.14, "hello"
print(num1)
print(num2)
print(f1)
print(str1)

